#include "ft_printf.h"

void	ft_putstr(char *s, int *l)
{
	int	i;

	i = 0;
	if (!s)
	{
		ft_putstr("(null)", l);
	}
	else
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
			(*l)++;
		}	
	}
}
